const scenario = new Level(ctx2, level1);

const player = new Player(ctx, ctx2, scenario, 60, 120);

function init() {
  setInterval(() => {
    main();
  }, 1000 / FPS);
}

function drawCeilFloor() {
  ctx.fillStyle = "#666";
  ctx.fillRect(0, 0, widthCanvas, heightCanvas / 2);
  ctx.fillStyle = "#752300";
  ctx.fillRect(0, heightCanvas / 2, widthCanvas, heightCanvas / 2);
}
function clearCanvas() {
  canvas.width = widthCanvas;
  canvas.height = heightCanvas;
}

function update() {
  player.update();
}

function main() {
  updateStats();
  clearCanvas();
  update();
  draw();
}

function countFPS() {
  let thisLoop = Date.now();
  let fps = 1000 / (thisLoop - lastLoop);
  lastLoop = thisLoop;

  return fps;
}

function updateStats() {
  document.querySelector("#fpsCounter").innerHTML = `FPS: ${Math.round(
    countFPS()
  )}`;
  document.querySelector("#xPosition").innerHTML = `X: ${Math.round(player.x)}`;
  document.querySelector("#yPosition").innerHTML = `Y: ${Math.round(player.y)}`;
}

function draw() {
  drawCeilFloor();
  scenario.draw();
  player.draw();
}

window.onload = init;
